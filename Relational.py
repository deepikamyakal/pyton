# 2.Write a program to compare two numbers using relational operators and display the result.

a = int(input("Enter first no :"))
b = int(input("Enter second no :"))

print("a > b :", a > b)
print("a < b :", a < b)
print("a == b", a == b)
print("a != b", a != b)
print("a >= b:", a >= b)
print("a <= b :", a <= b)
